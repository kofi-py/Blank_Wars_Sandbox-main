// Test character retrieval for the user
function testCharacterRetrieval(userId) {
  console.log('\nğŸ­ Testing character retrieval for user:', userId);

  const charOptions = {
    hostname: 'localhost',
    port: 4000,
    path: `/api/characters/user/${userId}`,
    method: 'GET',
    headers: {
      'Content-Type': 'application/json'
    }
  };

  const charReq = http.request(charOptions, (res) => {
    console.log(`ğŸ“Š Character endpoint status: ${res.statusCode}`);

    let data = '';
    res.on('data', (chunk) => {
      data += chunk;
    });

    res.on('end', () => {
      try {
        const response = JSON.parse(data);
        console.log('ğŸ­ User characters:', response.length || 0);
        if (response.length > 0) {
          console.log('âœ… Starter pack characters generated successfully!');
          response.forEach((char, index) => {
            console.log(`  ${index + 1}. ${char.name} (${char.title})`);
          });
        } else {
          console.log('âŒ No characters found - starter pack generation failed');
        }
      } catch (e) {
        console.log('âŒ Character retrieval parse error:', e.message);
        console.log('Raw response:', data);
      }
    });
  });

  charReq.on('error', (e) => {
    console.error('âŒ Character request error:', e.message);
  });

  charReq.end();
}
